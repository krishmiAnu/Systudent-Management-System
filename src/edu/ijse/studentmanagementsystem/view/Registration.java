    private void loadData() {
        try {
            dtmRegistration.setRowCount(0);

            ArrayList<RegistrationDTO> all = ctrlRegistration.getAll();
            ArrayList<StudentDTO> allStudent = ctrlStudent.getAll();
            ArrayList<GradeDTO> allGrade = ctrlGrade.getAll();

            String student = "";
            String studentId = "";

            String grade = "";
            String gradeId = "";

            for (RegistrationDTO all1 : all) {
                studentId = all1.getSid();
                gradeId = all1.getGid();

            }
            for (StudentDTO all1 : allStudent) {
                if (all1.getSid().equals(studentId)) {
                    student = all1.getFullName();
                }
            }
            for (GradeDTO all2 : allGrade) {
                if (all2.getGradeId().equals(gradeId)) {
                    grade = all2.getGradeNo();
                }
            }

            if (all == null) {
                return;
            }

            for (RegistrationDTO registrationDTO : all) {
                Object[] rowData = {
                    registrationDTO.getRid(),
                    registrationDTO.getSid(),
                    registrationDTO.getGid(),
                    registrationDTO.getDate(),
                    registrationDTO.getFee()

                };
                dtmRegistration.addRow(rowData);
            }
        } catch (Exception ex) {
            Logger.getLogger(Registration.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

