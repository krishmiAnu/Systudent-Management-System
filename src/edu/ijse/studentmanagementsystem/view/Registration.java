    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            String registerId = txtRegister.getText();
            String sid = ctrlStudent.getIdByFullName(cmbStudentId.getSelectedItem().toString()).getSid();
            String gid = ctrlGrade.getIdByGradeNo(cmbGradeId.getSelectedItem().toString()).getGradeId();
            String date = txtDate.getText();
            Double fee = Double.parseDouble(txtFee.getText());

            RegistrationDTO registrationDTO = new RegistrationDTO(registerId, sid, gid, date, fee);

            try {
                boolean add = ctrlRegistration.add(registrationDTO);
                if (add) {
                    JOptionPane.showMessageDialog(this, "Success");
                    loadData();
                } else {
                    JOptionPane.showMessageDialog(this, "Something went wrong");
                }
            } catch (Exception ex) {
            }
        } catch (Exception ex) {
            Logger.getLogger(Registration.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

