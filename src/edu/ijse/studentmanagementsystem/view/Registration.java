    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String registrationId = dtmRegistration.getValueAt(tblRegistration.getSelectedRow(), 0).toString();
        try {
            boolean deleted = ctrlRegistration.delete(registrationId);
            if (deleted) {
                //clearFields();
                //setEnable(false);
                loadData();
                tblRegistration.clearSelection();
                //btnNewSubject.requestFocusInWindow();
            } else {

                JOptionPane.showMessageDialog(this, "Delete ");

            }
        } catch (Exception ex) {

            JOptionPane.showMessageDialog(this,
                    "Grade deletion has been failed, Please try again.",
                    "Delete Error",
                    JOptionPane.ERROR_MESSAGE
            );
            Logger.getLogger(Registration.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

