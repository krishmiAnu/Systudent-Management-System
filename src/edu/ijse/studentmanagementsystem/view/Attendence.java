    private void btnAdd1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdd1ActionPerformed
        String attendeceId = txtAttendenceId.getText();
        String rid = cmbRegisterId.getSelectedItem().toString();
        String sub_id = cmbSubjectId.getSelectedItem().toString();
        String date = txtDate.getText();
        Double time = (Double.parseDouble(txtTime.getText()));

        String present_mark = "";

        if (rbnPresent.isSelected()) {
            present_mark = "present";
        }

        if (rbnAbsent.isSelected()) {
            present_mark = "absent";

        }

        AttendenceDTO attendenceDTO = new AttendenceDTO(attendeceId, rid, sub_id, date, time, present_mark);

        try {

            boolean add = ctrlattendence.add(attendenceDTO);
            if (add) {

                JOptionPane.showMessageDialog(this, "Success");
                loadData();
            } else {

                JOptionPane.showMessageDialog(this, "something went wrong");
            }

        } catch (Exception ex) {
            Logger.getLogger(Attendence.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAdd1ActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            String attendeceId = txtAttendenceId.getText();
            String rid = cmbRegisterId.getSelectedItem().toString();
            String sub_id = cmbSubjectId.getSelectedItem().toString();
            String date = txtDate.getText();
            Double time = (Double.parseDouble(txtTime.getText()));

            String present_mark = "";

            if (rbnPresent.isSelected()) {
                present_mark = "present";
            }

            if (rbnAbsent.isSelected()) {
                present_mark = "absent";

            }

            AttendenceDTO attendenceDTO = new AttendenceDTO(attendeceId, rid, sub_id, date, time, present_mark);

            boolean isUpdate = ctrlattendence.update(attendenceDTO);
            if (isUpdate) {
                JOptionPane.showMessageDialog(Attendence.this, "Update Success...");
                loadData();
                //clearFields();
            } else {
                JOptionPane.showMessageDialog(Attendence.this, "Update Fail");
            }
        } catch (Exception ex) {
            Logger.getLogger(Attendence.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

