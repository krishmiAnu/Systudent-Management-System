    private void loadSubjectIds() {
        try {
            ArrayList<SubjectDTO> SubjectIdList = ctrlSubject.getAll();
            for (SubjectDTO subjectId : SubjectIdList) {
                cmbSubjectId.addItem(subjectId.getName());
            }
        } catch (Exception e) {
        }
    }

    private void loadRegisterIds() {
        try {
            ArrayList<RegistrationDTO> RegisterIdList = ctrlReg.getAll();
            for (RegistrationDTO registerId : RegisterIdList) {
                cmbRegisterId.addItem(registerId.getRid());
            }
        } catch (Exception e) {
        }
    }
