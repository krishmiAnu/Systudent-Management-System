    private void loadData() {
        try {
            //dtmSchedule.setRowCount(0);
            ctrlschedule = new ScheduleControllerImpl();
            ArrayList<ScheduleDTO> all = ctrlschedule.getAll();

            if (all == null) {
                return;
            }

            for (ScheduleDTO schedule: all) {
                Object[] rowData = {
                    schedule.getScheduleId(),
                    schedule.getGrade(),
                    schedule.getSubject(),
                    schedule.getRegisterId(),
                    schedule.getDay(),
                    schedule.getTime()
                    
                };
                dtmSchedule.addRow(rowData);
            }
        } catch (Exception ex) {
            Logger.getLogger(Schedule.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
