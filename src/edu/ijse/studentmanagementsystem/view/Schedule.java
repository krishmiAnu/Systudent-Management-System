    private void loadSubjectIds(){
        try {
            ArrayList<SubjectDTO>SubjectIdList=ctrlsubject.getAll();
            for(SubjectDTO subjectId:SubjectIdList){
                cmbSubject.addItem(subjectId.getName());
            }
        } catch (Exception e) {
        }
    }
    
    private void loadGradeIds(){
        try {
            ArrayList<GradeDTO>GradeIdList=ctrlgrade.getAll();
            for(GradeDTO gradeId:GradeIdList){
                cmbGrade.addItem(gradeId.getGradeNo());
            }
        } catch (Exception e) {
        }
    }
    private void loadTeacherIds(){
          try {
            ArrayList<TeacherDTO> teacherList=ctrlteacher.getAll();
            for(TeacherDTO teacherDTO:teacherList){
                cmbTeacherName.addItem(teacherDTO.getFull_name());
            }
        } catch (Exception e) {
        }
    }
    private void loadRegisterIds(){
        try {
            ArrayList<RegistrationDTO>registrationList=ctrlregistration.getAll();
            for(RegistrationDTO registrationDTO:registrationList){
                cmbRegisterId.addItem(registrationDTO.getRid());
            }
        } catch (Exception e) {
        }
    }
    

