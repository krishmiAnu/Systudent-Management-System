    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String scheduleId=txtSchedule.getText();
        String teacherName=cmbTeacherName.getSelectedItem().toString();
        String grade=cmbGrade.getSelectedItem().toString();
        String sub_id = cmbSubject.getSelectedItem().toString();
        String  rid= cmbRegisterId.getSelectedItem().toString();
        String date = txtDay.getText();
        Double time = (Double.parseDouble(txtTime.getText()));
        
        ScheduleDTO scheduleDTO = new ScheduleDTO(scheduleId,teacherName, grade, sub_id, rid, date, time);
        
        try {
            
            boolean add = ctrlschedule.add(scheduleDTO);
            if(add){
            
                JOptionPane.showMessageDialog(this, "Success");
                loadData();
            }else{
            
                    JOptionPane.showMessageDialog(this, "something went wrong");
            }
            
        } catch (Exception ex) {
            Logger.getLogger(Schedule.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddActionPerformed

