    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String scheduleId = dtmSchedule.getValueAt(tblSchedule.getSelectedRow(), 0).toString();
        try {
            boolean deleted = ctrlschedule.delete(scheduleId);
            if (deleted) {
                //clearFields();
                //setEnable(false);
                
                tblSchedule.clearSelection();
                //btnNewSubject.requestFocusInWindow();
                JOptionPane.showMessageDialog(this,"DELETE Success");
            } else {
                JOptionPane.showMessageDialog(this,
                    "Schedule deletion has been failed, Please try again.",
                    "Delete Error",
                    JOptionPane.ERROR_MESSAGE
            );
                
                
            }
        } catch (Exception ex) {

            Logger.getLogger(Schedule.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

