package lk.ijse.edu.dto;

/**
 *
 * @author Krishanthi
 */
public class UserHistoryDTO extends SuperDTO{
    private String userName;
    private String date;
    private String time;

    public UserHistoryDTO() {
    }

    public UserHistoryDTO(String userName, String date, String time) {
        this.userName = userName;
        this.date = date;
        this.time = time;
    }

    /**
     * @return the userName
     */
    public String getUserName() {
        return userName;
    }

    /**
     * @param userName the userName to set
     */
    public void setUserName(String userName) {
        this.userName = userName;
    }

    /**
     * @return the date
     */
    public String getDate() {
        return date;
    }

    /**
     * @param date the date to set
     */
    public void setDate(String date) {
        this.date = date;
    }

    /**
     * @return the time
     */
    public String getTime() {
        return time;
    }

    /**
     * @param time the time to set
     */
    public void setTime(String time) {
        this.time = time;
    }
    
}
